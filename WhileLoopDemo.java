//While loop Example
package opps;

public class WhileLoopDemo 
{
  public static void main(String args[])
  {
         int count = 1;
                         // Exits when count becomes greater than 5
         while (count <= 5)
           {
             System.out.println("Value of count is:" + count);
                                                                  //increment the value of count for next iteration
             count++;
            }
     }
}
//O/P=1 2 3 4 5